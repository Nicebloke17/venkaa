package com.venkat;

public class Switchdemo2 {
public static void main(String[] args) {
	int age = 30;
	switch (age) {
	case 10 : {
		
		System.out.println("10");
	}
	case 20 :{
		System.out.println("20");
	}
	default :{
		System.out.println("you not in 10 and 20");
	}
	}
	
}
}
